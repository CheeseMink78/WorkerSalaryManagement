
package model;
public class Worker {
    private String id,name;
    private int age,salary;
    public Worker(String id, String name, int age, int salary) {
        this.id = id;
        this.name = name;
        this.age = age;
        this.salary = salary;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public int getSalary() {
        return salary;
    }

    public void setSalary(int salary) {
        this.salary = salary;
    }

    @Override
    public String toString() {
        return String.format("%-10s | %-20s | %-10d | %-10d", id,name,age,salary);
    }
    public boolean equals(Worker o) {
        return this.id.matches(o.getId());
    }
}
